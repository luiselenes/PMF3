import $ from 'jquery';
$(document).ready(function(){
   
    $('#btnnext').on('click', function() {
        const arr = JSON.parse($('#routesData').val());
        let ind = parseInt($('#routesDataInd').val());
        if (arr.length <= ind)
            return 
        ind += 1;
        console.log(arr[ind].id)
        $('#imgRoute').attr('src', arr[ind].image.url)
        $('#routesDataInd').val(ind)
    });
})